// weather app using switch statement//


let weather = prompt("what is the weather outside");

switch (weather) {
    case 'sunny':
        console.log("wear shorts");
        break;

    case 'windy':
        console.log("its windy outside");
        break;

    case 'rainy':
        console.log("raining take umbrella");
        break;

    case 'snowy':
        console.log("wear jacket snowing outside");
        break;

    default:
        console.log("invalid weather options");
}


